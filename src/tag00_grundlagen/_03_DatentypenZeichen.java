package tag00_grundlagen;

public class _03_DatentypenZeichen {

	public static void main(String[] args) {
		char z1 = 'a';
		char z2 = 'h';
		char z3 = 97;
		
		System.out.println("z1: " + z1); //a
		System.out.println("z2: " + z2); //h
		System.out.println("z3: " + z3); //a, da 97 in der ASCII - Table a entspricht
		// entspricht auch im Unicode 97, da die ASCII Teil des Unicodes ist
		
		// https://www.asciitable.com

	}

}
