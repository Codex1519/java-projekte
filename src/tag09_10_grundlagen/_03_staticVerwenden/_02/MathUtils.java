package tag09_10_grundlagen._03_staticVerwenden._02;

public class MathUtils {
	
	// Utility-Methode, zur Berechnung des Maximums zweier Ganzzahlen
	public static int max(int a, int b) {
		return (a > b) ? a : b;
	}
	
	// Utitlity-Methode zur Berechnung des Quadrates einer Zahl
	public static int square(int a) {
		return a * a;
	}
}