package tag02_03_04_kontrollstrukturen._schleifen;

public class _02_forSchleife {

	public static void main(String[] args) {
		// For-Schleife eine besondere Form der kopfgesteuerten Schleife
		
		// Wiederholt die Anweisungen abhängig von Kontrollausdrücken im Schleifenkopf
		
		// Die Schleife wird häufig auch "Zählschleife" genannt
		
		// Die For-Schleife besteht aus drei Teilen: Der Initialisierungsblock (auch mehrere Initialisierung möglich, 
		// der Bedingunsblock (auch mehrere Bedingungen möglich) 
		
		for (int i = 0; i <= 5; i++) {
			System.out.println("Wert von i: " + i);
		}
	}
}
